package com.entity;

public class Dshop {
	private String dno;
	private String dname;
	private String dtype;
	private String dtel;
	private String dsafe;
	public String getDno() {
		return dno;
	}
	public void setDno(String dno) {
		this.dno = dno;
	}
	public String getDname() {
		return dname;
	}
	public void setDname(String dname) {
		this.dname = dname;
	}
	public String getDtype() {
		return dtype;
	}
	public void setDtype(String dtype) {
		this.dtype = dtype;
	}
	public String getDtel() {
		return dtel;
	}
	public void setDtel(String dtel) {
		this.dtel = dtel;
	}
	public String getDsafe() {
		return dsafe;
	}
	public void setDsafe(String dsafe) {
		this.dsafe = dsafe;
	}
	
}
