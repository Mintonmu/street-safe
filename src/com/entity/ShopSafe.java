package com.entity;

public class ShopSafe {
	private String sno;
	private String dno;
	private String xflevel;
	private String fwlevel;
	private String qtlevel;
	public String getSno() {
		return sno;
	}
	public void setSno(String sno) {
		this.sno = sno;
	}
	public String getDno() {
		return dno;
	}
	public void setDno(String dno) {
		this.dno = dno;
	}
	public String getXflevel() {
		return xflevel;
	}
	public void setXflevel(String xflevel) {
		this.xflevel = xflevel;
	}
	public String getFwlevel() {
		return fwlevel;
	}
	public void setFwlevel(String fwlevel) {
		this.fwlevel = fwlevel;
	}
	public String getQtlevel() {
		return qtlevel;
	}
	public void setQtlevel(String qtlevel) {
		this.qtlevel = qtlevel;
	}
	
}
